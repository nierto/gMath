// ============================================================================
// HELPER: Find MSB position (floor of log2)
// ============================================================================

/// Find the position of the most significant bit (0-indexed from LSB)
/// Returns None for x == 0
#[cfg(any(table_format = "q64_64", table_format = "q32_32", table_format = "q16_16"))]
#[inline(always)]
fn find_msb_position_i128(x: i128) -> Option<u32> {
    if x <= 0 {
        return None;
    }
    // 128 - 1 - leading_zeros gives position of MSB
    Some(127 - (x as u128).leading_zeros())
}

#[cfg(any(table_format = "q64_64", table_format = "q128_128", table_format = "q32_32", table_format = "q16_16"))]
#[inline(always)]
fn find_msb_position_i256(x: &I256) -> Option<u32> {
    if *x <= I256::zero() {
        return None;
    }
    // Check words from high to low
    for i in (0..4).rev() {
        if x.words[i] != 0 {
            let bit_pos = 63 - x.words[i].leading_zeros();
            return Some(i as u32 * 64 + bit_pos);
        }
    }
    None
}

#[cfg(any(table_format = "q128_128", table_format = "q256_256"))]
#[inline(always)]
fn find_msb_position_i512(x: &I512) -> Option<u32> {
    if *x <= I512::zero() {
        return None;
    }
    // Check words from high to low
    for i in (0..8).rev() {
        if x.words[i] != 0 {
            let bit_pos = 63 - x.words[i].leading_zeros();
            return Some(i as u32 * 64 + bit_pos);
        }
    }
    None
}

#[cfg(any(table_format = "q256_256", table_format = "q512_512"))]
#[inline(always)]
fn find_msb_position_i1024(x: &I1024) -> Option<u32> {
    if *x <= I1024::zero() {
        return None;
    }
    // Check words from high to low
    for i in (0..16).rev() {
        if x.words[i] != 0 {
            let bit_pos = 63 - x.words[i].leading_zeros();
            return Some(i as u32 * 64 + bit_pos);
        }
    }
    None
}

