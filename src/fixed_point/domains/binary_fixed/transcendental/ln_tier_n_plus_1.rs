#[cfg(any(table_format = "q64_64", table_format = "q128_128", table_format = "q32_32", table_format = "q16_16"))]
#[inline(always)]
fn multiply_i256_q128_128(a: I256, b: I256) -> I256 {
    // Handle signed multiplication: mul_to_i512 does UNSIGNED multiplication,
    // so we need to handle signs explicitly (same pattern as multiply_i512_q256_256)
    let a_negative = a < I256::zero();
    let b_negative = b < I256::zero();
    let result_negative = a_negative != b_negative;

    let abs_a = if a_negative { I256::zero() - a } else { a };
    let abs_b = if b_negative { I256::zero() - b } else { b };

    let result_i512 = abs_a.mul_to_i512(abs_b);
    let rounding = I512::from_i256(I256::from_i128(1)) << 127;
    let unsigned_result = ((result_i512 + rounding) >> 128).as_i256();

    if result_negative {
        I256::zero() - unsigned_result
    } else {
        unsigned_result
    }
}

/// Divide two I256 values in Q128.128 format
/// Result = (a / b) in Q128.128
#[inline(always)]
#[cfg(any(table_format = "q64_64", table_format = "q128_128", table_format = "q32_32", table_format = "q16_16"))]
fn divide_i256_q128_128(a: I256, b: I256) -> I256 {
    if b == I256::zero() {
        return I256::zero();
    }
    // To compute a/b in Q128.128:
    // Result = (a << 128) / b
    // But a << 128 would overflow I256, so we upscale to I512
    let a_i512 = I512::from_i256(a) << 128;
    let b_i512 = I512::from_i256(b);
    (a_i512 / b_i512).as_i256()
}

/// Taylor series for ln(1+r) in Q128.128 format
/// ln(1+r) = r - r²/2 + r³/3 - r⁴/4 + ... for |r| < 1
///
/// **INPUT**: r is the scaled remainder after 4-stage table lookup, |r| < 2^-40
/// **OUTPUT**: ln(1+r) in Q128.128 format
/// **PRECISION**: For |r| < 2^-40, 5 terms for 128+ bits (r^5 < 2^-200)
#[cfg(any(table_format = "q64_64", table_format = "q128_128", table_format = "q32_32", table_format = "q16_16"))]
#[inline(always)]
fn taylor_series_ln_i256_q128_128(r: &I256) -> I256 {
    if *r == I256::zero() {
        return I256::zero();
    }

    const TERMS: usize = 5; // |r| < 2^-40 → r^5 < 2^-200, well beyond 128-bit precision

    let mut result = *r; // First term: r
    let mut r_power = *r;

    for n in 2..=TERMS {
        // r^n = r^(n-1) × r
        r_power = multiply_i256_q128_128(r_power, *r);

        // Compute 1/n in Q128.128
        let one_q128 = I256::from_i128(1) << 128;
        let n_i256 = I256::from_i128(n as i128);
        let coef = divide_i256_q128_128(one_q128, n_i256 << 128);

        // term = r^n × (1/n)
        let term = multiply_i256_q128_128(r_power, coef);

        // Apply alternating signs: ln(1+r) = r - r²/2 + r³/3 - r⁴/4 + ...
        if n % 2 == 0 {
            result = result - term;
        } else {
            result = result + term;
        }
    }

    result
}

