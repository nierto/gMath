#[cfg(table_format = "q64_64")]
pub fn ln_binary_i128(x: i128) -> i128 {
    if x <= 0 {
        return i128::MIN;
    }
    // Tier N+1: compute ln at Q128.128, downscale to Q64.64
    let x_q128 = I256::from_i128(x) << 64;
    let result_q128 = ln_q128_128_native(x_q128);
    // Downscale Q128.128 → Q64.64 with rounding (0.5 ULP at Q64.64 = bit 63)
    let rounding = I256::from_i128(1i128 << 63);
    ((result_q128 + rounding) >> 64).as_i128()
}

#[cfg(table_format = "q64_64")]
pub fn ln_binary_i256(x: I256) -> I256 {
    if x <= I256::zero() {
        return I256::min_value();
    }
    // Tier N+1: compute ln at Q128.128 natively (tables available for all profiles)
    ln_q128_128_native(x)
}

#[cfg(table_format = "q64_64")]
pub fn ln_binary_i512(x: I512) -> I512 {
    if x <= I512::zero() {
        return I512::min_value();
    }
    // Q64.64 profile doesn't support I512 natively, downscale
    let x_q64 = (x >> 192).as_i256().as_i128();
    let result_q64 = ln_q64_64_native(x_q64);
    I512::from_i256(I256::from_i128(result_q64)) << 192
}

