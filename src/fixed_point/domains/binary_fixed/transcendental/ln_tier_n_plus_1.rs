/// Taylor series for ln(1+r) in Q256.256 format
/// ln(1+r) = r - r²/2 + r³/3 - r⁴/4 + ... for |r| < 1
///
/// **INPUT**: r is the scaled remainder after 3-stage table lookup, |r| < 2^-30
/// **OUTPUT**: ln(1+r) in Q256.256 format
/// **PRECISION**: For |r| < 2^-40, only ~7 terms needed for 77 decimals
///
/// **NOTE**: The table LN_TAYLOR_COEFFICIENTS_TIER_5 stores UNSIGNED coefficients (1/n).
/// Signs are applied here: subtract even terms, add odd terms.
/// This is necessary because I512 multiplication is unsigned.
#[cfg(any(table_format = "q128_128", table_format = "q256_256"))]
#[inline(always)]
fn taylor_series_ln_i512_q256_256(r: &I512) -> I512 {
    use crate::fixed_point::I1024;

    if *r == I512::zero() {
        return I512::zero();
    }

    // For very small r (< 2^-40 after 4-stage reduction), convergence is fast
    // Each term r^n/n decreases by factor of r (< 2^-40) per term
    // For 77 decimals (~256 bits), need 256/40 ≈ 7 terms
    const TERMS: usize = 8;  // Extra margin for safety

    let mut result = *r;  // First term: r (coefficient for n=1 is +1)
    let mut r_power = *r;

    for n in 2..=TERMS {
        // r^n = r^(n-1) × r
        r_power = multiply_i512_q256_256(r_power, *r);

        // Coefficient 1/n from table (UNSIGNED - signs applied below)
        let (coef_main, coef_comp) = LN_TAYLOR_COEFFICIENTS_TIER_5[n];
        let coef_comp_contrib = (I1024::from_i512(I512::from_i256(coef_comp)) >> 256).as_i512();
        let coef = coef_main + coef_comp_contrib;

        // term = r^n × (1/n) -- always positive
        let term = multiply_i512_q256_256(r_power, coef);

        // Apply alternating signs: ln(1+r) = r - r²/2 + r³/3 - r⁴/4 + ...
        // Even n: subtract (−r²/2, −r⁴/4, ...)
        // Odd n: add (+r³/3, +r⁵/5, ...)
        if n % 2 == 0 {
            result = result - term;
        } else {
            result = result + term;
        }

        // Early termination if term becomes negligible
        // For |r| < 2^-30, after 10 terms: |term| < (2^-30)^10 / 10 ≈ 2^-300
        // This is beyond our precision, so we can stop
    }

    result
}

/// Taylor series for ln(1+r) in Q512.512 format
/// ln(1+r) = r - r²/2 + r³/3 - r⁴/4 + ... for |r| < 1
///
/// **INPUT**: r is the scaled remainder after 4-stage table lookup, |r| < 2^-40
/// **OUTPUT**: ln(1+r) in Q512.512 format
/// **PRECISION**: For |r| < 2^-40, only ~13 terms needed for 154 decimals
///
/// **NOTE**: The table LN_TAYLOR_COEFFICIENTS_TIER_6 stores UNSIGNED coefficients (1/n).
/// Signs are applied here: subtract even terms, add odd terms.
/// This is necessary because I1024 multiplication is unsigned.
#[cfg(any(table_format = "q256_256", table_format = "q512_512"))]
#[inline(always)]
fn taylor_series_ln_i1024_q512_512(r: &I1024) -> I1024 {
    use crate::fixed_point::I2048;

    if *r == I1024::zero() {
        return I1024::zero();
    }

    // For |r| < 2^-40 after 4-stage reduction:
    // Each term r^n/n decreases by factor of r (< 2^-40) per term
    // For 154 decimals (~512 bits), need 512/40 ≈ 13 terms
    const TERMS: usize = 14;  // Extra margin for safety

    let mut result = *r;  // First term: r (coefficient for n=1 is +1)
    let mut r_power = *r;

    for n in 2..=TERMS {
        // r^n = r^(n-1) × r
        r_power = (r_power.mul_to_i2048(*r) >> 512).as_i1024();

        // Coefficient 1/n from table (UNSIGNED - signs applied below)
        let (coef_main, coef_comp) = LN_TAYLOR_COEFFICIENTS_TIER_6[n];
        let coef_comp_contrib = (I2048::from_i1024(I1024::from_i512(coef_comp)) >> 512).as_i1024();
        let coef = coef_main + coef_comp_contrib;

        // term = r^n × (1/n) -- always positive
        let term = (r_power.mul_to_i2048(coef) >> 512).as_i1024();

        // Apply alternating signs: ln(1+r) = r - r²/2 + r³/3 - r⁴/4 + ...
        // Even n: subtract (−r²/2, −r⁴/4, ...)
        // Odd n: add (+r³/3, +r⁵/5, ...)
        if n % 2 == 0 {
            result = result - term;
        } else {
            result = result + term;
        }
    }

    result
}

