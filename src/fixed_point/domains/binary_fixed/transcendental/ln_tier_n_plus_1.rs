// SCIENTIFIC PROFILE (Q256.256) - UGOD TIER N+1 WRAPPERS
// ============================================================================
// Storage tier → Compute tier → Downscale
// Q64.64 (i128)    → Q128.128 (I256)  → i128    (tier N+1)
// Q128.128 (I256)  → Q256.256 (I512)  → I256    (tier N+1)
// Q256.256 (I512)  → Q512.512 (I1024) → I512    (tier N+1)

#[cfg(table_format = "q256_256")]
pub fn ln_binary_i128(x: i128) -> i128 {
    if x <= 0 {
        return i128::MIN;
    }
    // UGOD Tier N+1: Q64.64 storage → compute at Q128.128 → downscale
    // Upscale Q64.64 → Q128.128
    let x_q128 = I256::from_i128(x) << 64;
    // Compute at Q128.128 (which internally uses Q256.256 tables in scientific profile)
    let x_q256 = I512::from_i256(x_q128) << 128;
    let result_q256 = ln_q256_256_native(x_q256);
    // Downscale Q256.256 → Q128.128 with rounding (0.5 ULP at Q128.128 = bit 127)
    let rounding_q128 = I512::from_i256(I256::from_i128(1)) << 127;
    let result_q128 = ((result_q256 + rounding_q128) >> 128).as_i256();
    // Downscale Q128.128 → Q64.64 with rounding (0.5 ULP at Q64.64 = bit 63)
    let rounding_q64 = I256::from_i128(1i128 << 63);
    ((result_q128 + rounding_q64) >> 64).as_i128()
}

