/// Upscale Q64.64 (i128) to Q128.128 (I256) for tier N+1 computation
#[inline(always)]
pub fn upscale_q64_to_q128(value: i128) -> I256 {
    // Q64.64 has 64 fractional bits
    // Q128.128 needs 128 fractional bits
    // Shift left by 64 to move from Q64.64 → Q128.128
    I256::from_i128(value) << 64
}

/// Upscale Q128.128 (I256) to Q256.256 (I512) for tier N+1 computation
#[inline(always)]
pub fn upscale_q128_to_q256(value: I256) -> I512 {
    // Q128.128 has 128 fractional bits
    // Q256.256 needs 256 fractional bits
    // Shift left by 128 to move from Q128.128 → Q256.256
    I512::from_i256(value) << 128
}

/// Upscale Q64.64 (i128) to Q256.256 (I512) for tier N+2 computation
#[inline(always)]
pub fn upscale_q64_to_q256(value: i128) -> I512 {
    // Q64.64 has 64 fractional bits
    // Q256.256 needs 256 fractional bits
    // Shift left by 192 to move from Q64.64 → Q256.256
    I512::from_i256(I256::from_i128(value)) << 192
}

