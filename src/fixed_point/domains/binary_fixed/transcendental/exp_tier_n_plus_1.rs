/// Multiply two I256 values in Q128.128 format
#[cfg(any(table_format = "q64_64", table_format = "q128_128"))]
#[inline(always)]
fn multiply_i256_q128_128(a: I256, b: I256) -> I256 {
    // I256 × I256 in Q128.128 format
    // Result has 256 fractional bits, shift right by 128 to get Q128.128
    let result_i512 = a.mul_to_i512(b);
    (result_i512 >> 128).as_i256()
}

/// Multiply two I512 values in Q256.256 format
///
/// **PRECISION**: Native I1024 multiplication - ZERO precision loss
/// **STRATEGY**: I512 × I512 → I1024 (tier N+1), then shift to Q256.256
/// Enables 65-75/77 decimals for scientific profile
#[cfg(any(table_format = "q128_128", table_format = "q256_256"))]
#[inline(always)]
fn multiply_i512_q256_256(a: I512, b: I512) -> I512 {
    use crate::fixed_point::I1024;

    // Native multiplication using I1024 - exact, zero loss
    // I512 × I512 = I1024 with all 1024 bits preserved
    let result_i1024 = a.mul_to_i1024(b);

    // Add rounding before shift (round-half-up for IEEE compliance)
    // Q512.512 format: bit 255 is the rounding position for Q256.256
    let rounding = I1024::from_i512(I512::from_i256(I256::from_i128(1))) << 255;

    // Shift right by 256 bits to convert Q512.512 → Q256.256
    // Upper 512 bits of I1024 become I512 result
    ((result_i1024 + rounding) >> 256).as_i512()
}
