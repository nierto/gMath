/// Taylor series for exp(x) in Q256.256 format
///
/// **INPUT**: (u128, u128) remainder - (high 98 bits, low 128 bits) = 226 bits total
///            Represents bits 0-225 of the 256-bit fractional part
/// **OUTPUT**: I512 value in Q256.256 format
/// **PRECISION**: 35 terms for 77-decimal precision
/// **FORMULA**: exp(r) = 1 + r + r²/2! + r³/3! + ... + r³⁴/34!
#[cfg(any(table_format = "q128_128", table_format = "q256_256"))]
#[inline(always)]
fn taylor_series_q256_256(remainder: (u128, u128)) -> I512 {
    let (remainder_high, remainder_low) = remainder;

    // Handle exp(0) = 1
    if remainder_high == 0 && remainder_low == 0 {
        return I512::from_i256(I256::from_i128(1)) << 256;  // 1.0 in Q256.256
    }

    // Convert 226-bit remainder to I512 Q256.256 format
    // remainder_high: bits 128-225 (98 bits) → position at bits 128-225 of I512
    // remainder_low: bits 0-127 (128 bits) → position at bits 0-127 of I512
    // Result: bits 0-225 populated, bits 226-511 are zero (fractional value < 2^-30)

    // Build the I512 value:
    // Lower I256: (remainder_high << 128) | remainder_low
    let lower_i256 = (I256::from_u128(remainder_high) << 128) | I256::from_u128(remainder_low);
    let r = I512::from_i256(lower_i256);

    // Use Horner's method: exp(r) = 1 + r(1 + r(1/2! + r(1/3! + ...)))
    // Start from the last term and work backwards
    const TERMS: usize = 35;  // 35 terms for 77-decimal precision
    
    // Start with 1/34! (last term)
    let (result_main, result_comp) = FACTORIAL_RECIPROCALS_TIER_5[TERMS - 1];
    let mut result = result_main + (crate::fixed_point::I1024::from_i512(I512::from_i256(result_comp)) >> 256).as_i512();

    // Horner's method: work backwards from term 33 down to term 1
    for i in (1..TERMS - 1).rev() {
        // result = factorial_reciprocal[i] + r * result
        // multiply_i512_q256_256 handles Q256.256 multiplication correctly
        let product = multiply_i512_q256_256(r, result);

        let (fac_main, fac_comp) = FACTORIAL_RECIPROCALS_TIER_5[i];
        let fac = fac_main + (crate::fixed_point::I1024::from_i512(I512::from_i256(fac_comp)) >> 256).as_i512();

        result = fac + product;
    }

    // Final step: 1 + r * result
    let final_product = multiply_i512_q256_256(r, result);
    let one = I512::from_i256(I256::from_i128(1)) << 256;
    
    one + final_product
}

/// Q512.512 native exponential (Tier 6 - for computing tier 5 with tier N+1)
///
/// **INPUT**: I1024 value in Q512.512 format
/// **OUTPUT**: I1024 value in Q512.512 format
/// **PRECISION**: ~154 correct decimal digits
/// **TABLES**: Uses Q512.512 tables directly (TIER_6)
/// **PURPOSE**: Enables 70+ decimal precision for non-binary-exact inputs like 2.7
#[cfg(any(table_format = "q256_256", table_format = "q512_512"))]
pub fn exp_q512_512_native(x: crate::fixed_point::I1024) -> crate::fixed_point::I1024 {
    use crate::fixed_point::{I1024, I2048};

    // Handle exp(0) = 1
    if x == I1024::zero() {
        return I1024::from_i512(I512::from_i256(I256::from_i128(1))) << 512;
    }

    // Split into integer and fractional parts
    // Q512.512: upper 512 bits are integer, lower 512 bits are fraction
    let int_part = (x >> 512).as_i512().as_i256().as_i128();
    let frac_part = x.as_i512();  // Lower 512 bits

    // Integer part: Direct table lookup using tier 6 tables
    let (exp_int_main, exp_int_comp) = if int_part < -40 {
        return I1024::zero();  // Underflow
    } else if int_part > 40 {
        return I1024::max_value();  // Overflow
    } else {
        EXP_INTEGER_TABLE_TIER_6[(int_part + 40) as usize]
    };
    // Compensation term scaled by 2^256 in build.rs, shift right by 512 for Q512.512
    let exp_int = exp_int_main + (I2048::from_i1024(I1024::from_i512(exp_int_comp)) >> 512).as_i1024();

    // Fractional part: Three-stage table lookup
    if frac_part == I512::zero() {
        return exp_int;
    }

    // Extract indices from top 30 bits of 512-bit fractional part
    // frac_part is I512 (lower 512 bits of I1024)
    // We need bits 482-511 for table indices (30 bits total)
    // And bits 0-481 for Taylor remainder (482 bits)

    // Get upper 256 bits of fractional part for bit extraction
    let frac_upper = (frac_part >> 256).as_i256();
    let frac_u128 = (frac_upper >> 128).as_u128();  // Upper 128 bits of upper 256

    let k = (frac_u128 >> 118) as usize;  // Top 10 bits (bits 502-511 of full 512)
    let r1 = frac_u128 & ((1u128 << 118) - 1);
    let j = (r1 >> 108) as usize;  // Next 10 bits (bits 492-501)
    let r2 = r1 & ((1u128 << 108) - 1);
    let m = (r2 >> 98) as usize;  // Next 10 bits (bits 482-491)

    // Remainder: bits 0-481 (482 bits total)
    // We need to construct the remainder WITHOUT sign extension
    // The issue: I512::from_i256 and I1024::from_i512 sign-extend, corrupting unsigned data
    let remainder_mask_high = (1u128 << 98) - 1;
    let remainder_bits_high = r2 & remainder_mask_high;  // Bits 384-481 (98 bits) within frac_u128

    // Get bits 256-383 from frac_upper (lower 128 bits)
    let remainder_bits_mid = frac_upper.as_u128();  // Bits 256-383 of frac_part

    // Get bits 0-255 from frac_part (lower 256 bits)
    let frac_part_lower = frac_part.as_i256();  // Bits 0-255 of frac_part

    let (t1_main, t1_comp) = EXP_PRIMARY_TABLE_TIER_6[k];
    let (t2_main, t2_comp) = EXP_SECONDARY_TABLE_TIER_6[j];
    let (t3_main, t3_comp) = EXP_TERTIARY_TABLE_TIER_6[m];

    // Compensation terms scaled by 2^256 in build.rs, shift right by 512
    let t1 = t1_main + (I2048::from_i1024(I1024::from_i512(t1_comp)) >> 512).as_i1024();
    let t2 = t2_main + (I2048::from_i1024(I1024::from_i512(t2_comp)) >> 512).as_i1024();
    let t3 = t3_main + (I2048::from_i1024(I1024::from_i512(t3_comp)) >> 512).as_i1024();

    // STAGE 4: Taylor series for tiny remainder
    // Build remainder as I1024 in Q512.512 format (value < 2^-30)
    // CRITICAL: Use from_words to avoid sign extension!
    let remainder_i1024 = {
        // Layout in I1024 (16 words of 64 bits each, little-endian):
        // words[0-3]: bits 0-255 (frac_part_lower)
        // words[4-5]: bits 256-383 (remainder_bits_mid, u128 = 2 words)
        // words[6]: bits 384-447 (lower 64 bits of remainder_bits_high)
        // words[7]: bits 448-481 (upper 34 bits of remainder_bits_high, masked)
        // words[8-15]: 0 (no integer part for remainder)

        let w0 = frac_part_lower.words[0];
        let w1 = frac_part_lower.words[1];
        let w2 = frac_part_lower.words[2];
        let w3 = frac_part_lower.words[3];
        let w4 = remainder_bits_mid as u64;
        let w5 = (remainder_bits_mid >> 64) as u64;
        let w6 = remainder_bits_high as u64;
        let w7 = (remainder_bits_high >> 64) as u64;  // Only 34 bits valid (98-64=34)

        I1024::from_words([
            w0, w1, w2, w3, w4, w5, w6, w7,
            0, 0, 0, 0, 0, 0, 0, 0  // Integer part is zero for remainder
        ])
    };

    let exp_tiny = taylor_series_q512_512(remainder_i1024);

    // Multiply: exp_frac = t1 * t2 * t3 * exp_tiny
    // Use I2048 intermediates for full precision
    let p1 = (t1.mul_to_i2048(t2) >> 512).as_i1024();
    let p2 = (p1.mul_to_i2048(t3) >> 512).as_i1024();
    let exp_frac = (p2.mul_to_i2048(exp_tiny) >> 512).as_i1024();

    // Final: exp(x) = exp(int) * exp(frac)
    let result = (exp_int.mul_to_i2048(exp_frac) >> 512).as_i1024();

    result
}

/// Taylor series for exp(x) in Q512.512 format
///
/// **INPUT**: I1024 remainder in Q512.512 format (value < 2^-30)
/// **OUTPUT**: I1024 value in Q512.512 format
/// **PRECISION**: 50 terms for 154-decimal precision
/// **FORMULA**: exp(r) = 1 + r + r²/2! + r³/3! + ... + r⁴⁹/49!
#[cfg(any(table_format = "q256_256", table_format = "q512_512"))]
#[inline(always)]
fn taylor_series_q512_512(r: crate::fixed_point::I1024) -> crate::fixed_point::I1024 {
    use crate::fixed_point::{I1024, I2048};

    // Handle exp(0) = 1
    if r == I1024::zero() {
        return I1024::from_i512(I512::from_i256(I256::from_i128(1))) << 512;  // 1.0 in Q512.512
    }

    // Use Horner's method: exp(r) = 1 + r(1 + r(1/2! + r(1/3! + ...)))
    // Start from the last term and work backwards
    const TERMS: usize = 50;  // 50 terms for 154-decimal precision

    // Start with 1/49! (last term)
    let (result_main, result_comp) = FACTORIAL_RECIPROCALS_TIER_6[TERMS - 1];
    let mut result = result_main + (I2048::from_i1024(I1024::from_i512(result_comp)) >> 512).as_i1024();

    // Horner's method: work backwards from term 48 down to term 1
    for i in (1..TERMS - 1).rev() {
        // result = factorial_reciprocal[i] + r * result
        let product = (r.mul_to_i2048(result) >> 512).as_i1024();

        let (fac_main, fac_comp) = FACTORIAL_RECIPROCALS_TIER_6[i];
        let fac = fac_main + (I2048::from_i1024(I1024::from_i512(fac_comp)) >> 512).as_i1024();

        result = fac + product;
    }

    // Final step: 1 + r * result
    let final_product = (r.mul_to_i2048(result) >> 512).as_i1024();
    let one = I1024::from_i512(I512::from_i256(I256::from_i128(1))) << 512;

    one + final_product
}

/// Wrapper for exp_binary_i1024 - computes exp() at Q512.512 precision
#[cfg(any(table_format = "q256_256", table_format = "q512_512"))]
pub fn exp_binary_i1024(x: crate::fixed_point::I1024) -> crate::fixed_point::I1024 {
    exp_q512_512_native(x)
}
