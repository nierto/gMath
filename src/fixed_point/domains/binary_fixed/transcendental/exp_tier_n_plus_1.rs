/// Q64.64 native exponential (Tier 3 baseline)
///
/// **INPUT**: i128 value in Q64.64 format
/// **OUTPUT**: i128 value in Q64.64 format
/// **PRECISION**: ~10-13 correct digits (baseline implementation)
/// **TABLES**: Uses Q64.64 tables directly (no conversion)
#[inline(always)]
#[cfg(table_format = "q64_64")]
pub fn exp_q64_64_native(x: i128) -> i128 {
    // Tables are included directly at module level, use them without super::
    // Note: Q64.64 tables use TIER_3 naming (NOT Q64_64)
    use crate::fixed_point::multiply_binary_i128;

    // Handle exp(0) = 1
    if x == 0 {
        return 1i128 << 64;
    }

    // Split into integer and fractional parts
    let int_part = x >> 64;
    let frac_part = (x & ((1i128 << 64) - 1)) as u128;

    // Integer part: Direct table lookup (no conversion)
    let (exp_int_main, exp_int_comp) = if int_part < -40 {
        return 0; // Underflow
    } else if int_part > 40 {
        return i128::MAX; // Overflow
    } else {
        EXP_INTEGER_TABLE_TIER_3[(int_part + 40) as usize]
    };
    let exp_int = exp_int_main + ((exp_int_comp as i128) >> 32);

    // Fractional part: Three-stage table lookup (no conversion)
    if frac_part == 0 {
        return exp_int;
    }

    let k = (frac_part >> 54) as usize;  // Top 10 bits
    let r1 = frac_part & ((1u128 << 54) - 1);
    let j = (r1 >> 44) as usize;
    let r2 = r1 & ((1u128 << 44) - 1);
    let m = (r2 >> 34) as usize;
    let tiny = r2 & ((1u128 << 34) - 1);

    let (t1_main, t1_comp) = EXP_PRIMARY_TABLE_TIER_3[k];
    let (t2_main, t2_comp) = EXP_SECONDARY_TABLE_TIER_3[j];
    let (t3_main, t3_comp) = EXP_TERTIARY_TABLE_TIER_3[m];

    let t1 = t1_main + ((t1_comp as i128) >> 32);
    let t2 = t2_main + ((t2_comp as i128) >> 32);
    let t3 = t3_main + ((t3_comp as i128) >> 32);

    // Taylor series for tiny remainder (simplified for native tier)
    let exp_tiny = if tiny == 0 {
        1i128 << 64
    } else {
        // 1 + x + x²/2 (fast approximation for tiny values)
        let x_q64 = tiny as i128;
        let x2 = multiply_binary_i128(x_q64, x_q64);
        (1i128 << 64) + x_q64 + (x2 >> 1)
    };

    // Multiply: exp_frac = t1 * t2 * t3 * exp_tiny
    let p1 = multiply_binary_i128(t1, t2);
    let p2 = multiply_binary_i128(p1, t3);
    let exp_frac = multiply_binary_i128(p2, exp_tiny);

    // Final: exp(x) = exp(int) * exp(frac)
    multiply_binary_i128(exp_int, exp_frac)
}

/// Q128.128 native exponential (Tier 4 - for computing tier 3 with tier N+1)
///
/// **INPUT**: I256 value in Q128.128 format
/// **OUTPUT**: I256 value in Q128.128 format
/// **PRECISION**: ~16-20 correct digits (exceeds f64)
/// **TABLES**: Uses Q128.128 tables directly (TIER_4 tables with (I256, i128) format)
#[inline(always)]
#[cfg(any(table_format = "q64_64", table_format = "q128_128"))]
pub fn exp_q128_128_native(x: I256) -> I256 {
    // Tables are included directly at module level
    // Q128.128 tables use TIER_4 naming with (I256, i128) format

    // Handle exp(0) = 1
    if x == I256::zero() {
        return I256::from_i128(1) << 128;
    }

    // Split into integer and fractional parts
    let int_part = (x >> 128).as_i128();
    let frac_part = x.as_u128();  // Lower 128 bits

    // Integer part: Direct table lookup (tables are (I256, i128) in Q128.128 profile)
    let (exp_int_main, exp_int_comp) = if int_part < -40 {
        return I256::zero();
    } else if int_part > 40 {
        return I256::from_i128(i128::MAX) << 128;
    } else {
        EXP_INTEGER_TABLE_TIER_4[(int_part + 40) as usize]
    };
    // Compensation scaled by 2^64 in build.rs for TIER_4, shift right by 64
    let exp_int = exp_int_main + I256::from_i128(exp_int_comp >> 64);

    // Fractional part: Three-stage table lookup
    if frac_part == 0 {
        return exp_int;
    }

    let k = (frac_part >> 118) as usize;  // Top 10 bits
    let r1 = frac_part & ((1u128 << 118) - 1);
    let j = (r1 >> 108) as usize;
    let r2 = r1 & ((1u128 << 108) - 1);
    let m = (r2 >> 98) as usize;
    let tiny = r2 & ((1u128 << 98) - 1);

    let (t1_main, t1_comp) = EXP_PRIMARY_TABLE_TIER_4[k];
    let (t2_main, t2_comp) = EXP_SECONDARY_TABLE_TIER_4[j];
    let (t3_main, t3_comp) = EXP_TERTIARY_TABLE_TIER_4[m];

    // Compensation scaled by 2^64 in build.rs for TIER_4, shift right by 64
    let t1 = t1_main + I256::from_i128(t1_comp >> 64);
    let t2 = t2_main + I256::from_i128(t2_comp >> 64);
    let t3 = t3_main + I256::from_i128(t3_comp >> 64);

    // Taylor series for tiny remainder
    let exp_tiny = if tiny == 0 {
        I256::from_i128(1) << 128
    } else {
        let x_i256 = I256::from_u128(tiny);
        let x2 = multiply_i256_q128_128(x_i256, x_i256);
        (I256::from_i128(1) << 128) + x_i256 + (x2 >> 1)
    };

    // Multiply: exp_frac = t1 * t2 * t3 * exp_tiny
    let p1 = multiply_i256_q128_128(t1, t2);
    let p2 = multiply_i256_q128_128(p1, t3);
    let exp_frac = multiply_i256_q128_128(p2, exp_tiny);

    // Final: exp(x) = exp(int) * exp(frac)
    multiply_i256_q128_128(exp_int, exp_frac)
}

/// Q256.256 native exponential (Tier 5 - for computing tier 4 with tier N+1)
///
/// **INPUT**: I512 value in Q256.256 format
/// **OUTPUT**: I512 value in Q256.256 format
/// **PRECISION**: ~30+ correct digits (far exceeds f64)
/// **TABLES**: Uses Q256.256 tables directly (no conversion)
#[inline(always)]
#[cfg(any(table_format = "q128_128", table_format = "q256_256"))]
pub fn exp_q256_256_native(x: I512) -> I512 {
    // Use tier 5 tables (Q256.256 format)
    // Tables are already included at module level

    // Handle exp(0) = 1
    if x == I512::zero() {
        return I512::from_i256(I256::from_i128(1)) << 256;
    }

    // Split into integer and fractional parts
    let int_part = (x >> 256).as_i128();
    let frac_part = x.as_i256();  // Lower 256 bits

    // Integer part: Direct table lookup using tier 5 tables
    let (exp_int_main, exp_int_comp) = if int_part < -40 {
        return I512::zero();
    } else if int_part > 40 {
        return I512::from_i256(I256::from_i128(i128::MAX)) << 256;
    } else {
        EXP_INTEGER_TABLE_TIER_5[(int_part + 40) as usize]
    };
    // Compensation term scaled by 2^128 in build.rs, shift right by 256
    let exp_int = exp_int_main + (crate::fixed_point::I1024::from_i512(I512::from_i256(exp_int_comp)) >> 256).as_i512();

    // Fractional part: Three-stage table lookup
    if frac_part == I256::zero() {
        return exp_int;
    }

    // Extract indices from top 30 bits of 256-bit fractional part
    // frac_part is I256 (lower 256 bits of I512)
    // We need bits 226-255 for table indices (30 bits total)
    // And bits 0-225 for Taylor remainder (226 bits)

    // Get upper 128 bits of fractional part for bit extraction
    let frac_u128 = (frac_part >> 128).as_u128();  // Upper 128 bits

    let k = (frac_u128 >> 118) as usize;  // Top 10 bits (bits 246-255 of full 256)
    let r1 = frac_u128 & ((1u128 << 118) - 1);
    let j = (r1 >> 108) as usize;  // Next 10 bits (bits 236-245)
    let r2 = r1 & ((1u128 << 108) - 1);
    let m = (r2 >> 98) as usize;  // Next 10 bits (bits 226-235)

    // For remainder, we need bits 0-225 (226 bits total)
    // Upper 98 bits come from r2 (bits 128-225)
    // Lower 128 bits come from lower half of frac_part (bits 0-127)
    let remainder_high = r2 & ((1u128 << 98) - 1);  // Bits 128-225 (98 bits)
    let remainder_low = frac_part.as_u128();  // Bits 0-127 (128 bits)

    // Combine into full 226-bit remainder
    // Note: We need to pass both parts to Taylor series
    let remainder = (remainder_high, remainder_low);

    let (t1_main, t1_comp) = EXP_PRIMARY_TABLE_TIER_5[k];
    let (t2_main, t2_comp) = EXP_SECONDARY_TABLE_TIER_5[j];
    let (t3_main, t3_comp) = EXP_TERTIARY_TABLE_TIER_5[m];

    // Compensation terms scaled by 2^128 in build.rs, shift right by 256
    let t1 = t1_main + (crate::fixed_point::I1024::from_i512(I512::from_i256(t1_comp)) >> 256).as_i512();
    let t2 = t2_main + (crate::fixed_point::I1024::from_i512(I512::from_i256(t2_comp)) >> 256).as_i512();
    let t3 = t3_main + (crate::fixed_point::I1024::from_i512(I512::from_i256(t3_comp)) >> 256).as_i512();

    // STAGE 4: Taylor series for tiny remainder (35 terms, 77-decimal precision)
    let exp_tiny = taylor_series_q256_256(remainder);

    // Multiply: exp_frac = t1 * t2 * t3 * exp_tiny
    // PRECISION FIX: Use I1024 intermediates to prevent truncation loss!
    // Convert I512 Q256.256 → I1024 Q512.512 (upscale by 256 bits)
    use crate::fixed_point::I1024;
    let t1_i1024 = I1024::from_i512(t1) << 256;
    let t2_i1024 = I1024::from_i512(t2) << 256;
    let t3_i1024 = I1024::from_i512(t3) << 256;
    let exp_tiny_i1024 = I1024::from_i512(exp_tiny) << 256;

    // Multiply in I2048, downscale to I1024 Q512.512 (keeps full precision)
    // I1024 × I1024 = I2048, shift by 512 to get Q512.512 result
    let p1 = (t1_i1024.mul_to_i2048(t2_i1024) >> 512).as_i1024();
    let p2 = (p1.mul_to_i2048(t3_i1024) >> 512).as_i1024();
    let exp_frac = (p2.mul_to_i2048(exp_tiny_i1024) >> 512).as_i1024();

    // Final: exp(x) = exp(int) * exp(frac)
    let exp_int_i1024 = I1024::from_i512(exp_int) << 256;
    let result_i1024 = (exp_int_i1024.mul_to_i2048(exp_frac) >> 512).as_i1024();

    // Downscale I1024 Q512.512 → I512 Q256.256 with rounding
    // Add 0.5 ULP (bit 255) before shifting for round-half-up
    let rounding = I1024::from_i512(I512::from_i256(I256::from_i128(1))) << 255;
    ((result_i1024 + rounding) >> 256).as_i512()
}

