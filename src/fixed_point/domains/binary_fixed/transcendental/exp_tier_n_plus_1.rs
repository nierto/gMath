/// Downscale Q128.128 (I256) to Q64.64 (i128) with PROPER ROUNDING
///
/// **GUARANTEE**: Result is correctly rounded to nearest Q64.64 value (≤0.5 ULP)
#[inline(always)]
pub fn downscale_q128_to_q64(value: I256) -> i128 {
    // Q128.128: 128 integer bits + 128 fractional bits in I256
    // Q64.64:   64 integer bits + 64 fractional bits in i128
    //
    // Round-half-up: add 0.5 ULP at target precision before shift
    // 0.5 ULP at Q64.64 = 2^63 at Q128.128 position = bit 63
    let rounding = I256::from_i128(1i128 << 63);
    ((value + rounding) >> 64).as_i128()
}

/// Downscale Q256.256 (I512) to Q128.128 (I256) with PROPER ROUNDING
///
/// **GUARANTEE**: Result is correctly rounded to nearest Q128.128 value (≤0.5 ULP)
#[inline(always)]
pub fn downscale_q256_to_q128(value: I512) -> I256 {
    // Q256.256: 256 integer bits + 256 fractional bits in I512
    // Q128.128: 128 integer bits + 128 fractional bits in I256
    //
    // Round-half-up: add 0.5 ULP at target precision before shift
    // 0.5 ULP at Q128.128 = 2^127 at Q256.256 position = bit 127
    let rounding = I512::from_i256(I256::from_i128(1)) << 127;
    ((value + rounding) >> 128).as_i256()
}

/// Downscale Q256.256 (I512) to Q64.64 (i128) with PROPER ROUNDING
///
/// **GUARANTEE**: Result is correctly rounded to nearest Q64.64 value (≤0.5 ULP)
#[inline(always)]
pub fn downscale_q256_to_q64(value: I512) -> i128 {
    // Q256.256: 256 integer bits + 256 fractional bits in I512
    // Q64.64:   64 integer bits + 64 fractional bits in i128
    //
    // Round-half-up: add 0.5 ULP at target precision before shift
    // 0.5 ULP at Q64.64 = 2^63 at Q256.256 position = bit 191
    let rounding = I512::from_i256(I256::from_i128(1)) << 191;
    ((value + rounding) >> 192).as_i256().as_i128()
}

