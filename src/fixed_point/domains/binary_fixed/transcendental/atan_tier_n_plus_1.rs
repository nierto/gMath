pub fn atan2_binary_i512(y: I512, x: I512) -> I512 {
    // Downscale to Q128.128, compute, upscale back
    let y_q128 = (y >> 128).as_i256();
    let x_q128 = (x >> 128).as_i256();
    let result_q128 = atan2_q128_128_for_embedded(y_q128, x_q128);
    I512::from_i256(result_q128) << 128
}

#[cfg(any(table_format = "q64_64", table_format = "q32_32", table_format = "q16_16"))]
fn atan_q64_64(x: i128) -> i128 {
    let one_q64: i128 = 1i128 << 64;

    if x == 0 {
        return 0;
    }

    let is_neg = x < 0;
    let abs_x = if is_neg { -x } else { x };

    // Reduction 1: if |x| > 1, use atan(x) = π/2 - atan(1/x)
    // NOTE: Call atan_q64_64 recursively (not atan_core directly) so that
    // inv_x gets the tan(π/8) reduction too — ensures convergence for all inputs.
    if abs_x > one_q64 {
        // Compute 1/x in Q64.64
        let inv_x = {
            let one_wide = I256::from_i128(one_q64) << 64;
            let x_wide = I256::from_i128(abs_x);
            (one_wide / x_wide).as_i128()
        };
        let atan_inv = atan_q64_64(inv_x);
        let result = PI_HALF_Q64 - atan_inv;
        return if is_neg { -result } else { result };
    }

    // Reduction 2: if |x| > tan(π/8), use atan(x) = π/4 + atan((x-1)/(x+1))
    if abs_x > TAN_PI_8_Q64 {
        let num = abs_x - one_q64; // x - 1
        let den = abs_x + one_q64; // x + 1
        let reduced = {
            let num_wide = I256::from_i128(num) << 64;
            let den_wide = I256::from_i128(den);
            (num_wide / den_wide).as_i128()
        };
        let atan_reduced = atan_core_q64_64(reduced);
        let result = PI_QUARTER_Q64 + atan_reduced;
        return if is_neg { -result } else { result };
    }

    let result = atan_core_q64_64(abs_x);
    if is_neg { -result } else { result }
}

/// Core Taylor series for atan(x), |x| <= tan(π/8) ≈ 0.414
/// atan(x) = x - x³/3 + x⁵/5 - x⁷/7 + ...
#[cfg(any(table_format = "q64_64", table_format = "q32_32", table_format = "q16_16"))]
fn atan_core_q64_64(x: i128) -> i128 {
    let x_sq = {
        let x_wide = I256::from_i128(x);
        ((x_wide * x_wide) >> 64).as_i128()
    };

    // Horner form: atan(x) = x * (1 - x²/3 + x⁴/5 - x⁶/7 + ...)
    // = x * (1 - x²*(1/3 - x²*(1/5 - x²*(1/7 - ...))))
    // For Q64.64 (64 fractional bits), with |x_sq| ≤ 0.1716:
    // Need n such that 0.1716^n < 2^{-64} → n > 64*ln(2)/ln(1/0.1716) ≈ 25.2
    let num_terms = 27;
    let mut result: i128 = 0;

    // Build from inside out
    for k in (1..num_terms).rev() {
        let n = 2 * k + 1; // 3, 5, 7, ...
        // Coefficient: 1/n in Q64.64
        let coeff = (1i128 << 64) / (n as i128);
        let term = {
            let x_sq_wide = I256::from_i128(x_sq);
            let result_wide = I256::from_i128(result);
            ((x_sq_wide * result_wide) >> 64).as_i128()
        };
        result = coeff - term;
    }

    // Final: atan(x) = x * (1 - x² * result)
    let one_q64 = 1i128 << 64;
    let inner = {
        let x_sq_wide = I256::from_i128(x_sq);
        let result_wide = I256::from_i128(result);
        ((x_sq_wide * result_wide) >> 64).as_i128()
    };
    let poly = one_q64 - inner;

    let atan_val = {
        let x_wide = I256::from_i128(x);
        let poly_wide = I256::from_i128(poly);
        ((x_wide * poly_wide) >> 64).as_i128()
    };

    atan_val
}

/// atan2(y, x) in Q64.64 — handles all quadrants
#[cfg(any(table_format = "q64_64", table_format = "q32_32", table_format = "q16_16"))]
fn atan2_q64_64(y: i128, x: i128) -> i128 {
    if x == 0 && y == 0 {
        return 0; // undefined, return 0
    }

    if x > 0 {
        // atan(y/x)
        let ratio = {
            let y_wide = I256::from_i128(y) << 64;
            let x_wide = I256::from_i128(x);
            (y_wide / x_wide).as_i128()
        };
        return atan_q64_64(ratio);
    }

    if x < 0 && y >= 0 {
        // atan(y/x) + π
        let ratio = {
            let y_wide = I256::from_i128(y) << 64;
            let x_wide = I256::from_i128(x);
            (y_wide / x_wide).as_i128()
        };
        return atan_q64_64(ratio) + PI_Q64;
    }

    if x < 0 && y < 0 {
        // atan(y/x) - π
        let ratio = {
            let y_wide = I256::from_i128(y) << 64;
            let x_wide = I256::from_i128(x);
            (y_wide / x_wide).as_i128()
        };
        return atan_q64_64(ratio) - PI_Q64;
    }

    // x == 0
    if y > 0 {
        PI_HALF_Q64
    } else {
        -PI_HALF_Q64
    }
}

