#[cfg(any(table_format = "q64_64", table_format = "q128_128", table_format = "q32_32", table_format = "q16_16"))]
fn atan_q128_128(x: I256) -> I256 {
    let one_q128 = I256::from_i128(1) << 128;

    if x == I256::zero() {
        return I256::zero();
    }

    let is_neg = x < I256::zero();
    let abs_x = if is_neg { I256::zero() - x } else { x };

    if abs_x > one_q128 {
        let inv_x = {
            let one_wide = I512::from_i256(one_q128) << 128;
            let x_wide = I512::from_i256(abs_x);
            (one_wide / x_wide).as_i256()
        };
        let atan_inv = atan_q128_128(inv_x);
        let result = I256::from_words(PI_HALF_Q128) - atan_inv;
        return if is_neg { I256::zero() - result } else { result };
    }

    let tan_pi8 = I256::from_words(TAN_PI_8_Q128);
    if abs_x > tan_pi8 {
        let num = abs_x - one_q128;
        let den = abs_x + one_q128;
        let reduced = {
            let num_wide = I512::from_i256(num) << 128;
            let den_wide = I512::from_i256(den);
            (num_wide / den_wide).as_i256()
        };
        let atan_reduced = atan_core_q128_128(reduced);
        let result = I256::from_words(PI_QUARTER_Q128) + atan_reduced;
        return if is_neg { I256::zero() - result } else { result };
    }

    let result = atan_core_q128_128(abs_x);
    if is_neg { I256::zero() - result } else { result }
}

#[cfg(any(table_format = "q64_64", table_format = "q128_128", table_format = "q32_32", table_format = "q16_16"))]
fn atan_core_q128_128(x: I256) -> I256 {
    let x_sq = {
        let x_wide = I512::from_i256(x);
        ((x_wide * x_wide) >> 128).as_i256()
    };

    // For Q128.128 (128 fractional bits), with |x_sq| ≤ 0.1716:
    // Need n such that 0.1716^n < 2^{-128} → n > 128*ln(2)/ln(1/0.1716) ≈ 50.3
    let num_terms = 55;
    let mut result = I256::zero();

    for k in (1..num_terms).rev() {
        let n = 2 * k + 1;
        // 1/n in Q128.128
        let coeff = {
            let one_wide = I512::from_i256(I256::from_i128(1)) << 128;
            let n_wide = I512::from_i256(I256::from_i128(n as i128));
            (one_wide / n_wide).as_i256()
        };
        let term = {
            let x_sq_wide = I512::from_i256(x_sq);
            let result_wide = I512::from_i256(result);
            ((x_sq_wide * result_wide) >> 128).as_i256()
        };
        result = coeff - term;
    }

    let one_q128 = I256::from_i128(1) << 128;
    let inner = {
        let x_sq_wide = I512::from_i256(x_sq);
        let result_wide = I512::from_i256(result);
        ((x_sq_wide * result_wide) >> 128).as_i256()
    };
    let poly = one_q128 - inner;

    let x_wide = I512::from_i256(x);
    let poly_wide = I512::from_i256(poly);
    ((x_wide * poly_wide) >> 128).as_i256()
}

#[cfg(table_format = "q128_128")]
fn atan2_q128_128(y: I256, x: I256) -> I256 {
    let zero = I256::zero();
    if x == zero && y == zero {
        return zero;
    }

    if x > zero {
        let ratio = {
            let y_wide = I512::from_i256(y) << 128;
            let x_wide = I512::from_i256(x);
            (y_wide / x_wide).as_i256()
        };
        return atan_q128_128(ratio);
    }

    let pi = I256::from_words(PI_Q128);
    if x < zero && y >= zero {
        let ratio = {
            let y_wide = I512::from_i256(y) << 128;
            let x_wide = I512::from_i256(x);
            (y_wide / x_wide).as_i256()
        };
        return atan_q128_128(ratio) + pi;
    }

    if x < zero && y < zero {
        let ratio = {
            let y_wide = I512::from_i256(y) << 128;
            let x_wide = I512::from_i256(x);
            (y_wide / x_wide).as_i256()
        };
        return atan_q128_128(ratio) - pi;
    }

    let pi_half = I256::from_words(PI_HALF_Q128);
    if y > zero { pi_half } else { zero - pi_half }
}

