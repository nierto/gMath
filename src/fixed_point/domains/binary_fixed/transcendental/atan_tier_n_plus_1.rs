#[cfg(table_format = "q256_256")]
fn atan_q512_512(x: I1024) -> I1024 {
    let one_q512 = I1024::from_i128(1) << 512;

    let is_neg = (x.words[15] & 0x8000_0000_0000_0000) != 0;
    let abs_x = if is_neg { I1024::zero() - x } else { x };

    // Reduction 1: If |x| > 1, atan(x) = π/2 - atan(1/x)
    if abs_x > one_q512 {
        let inv_x = {
            use crate::fixed_point::domains::binary_fixed::i2048::i2048_div;
            let one_wide = I2048::from_i1024(one_q512) << 512;
            let x_wide = I2048::from_i1024(abs_x);
            i2048_div(one_wide, x_wide).as_i1024()
        };
        // Recurse through full reduction (inv_x < 1, so won't re-enter this branch)
        let atan_inv = atan_q512_512(inv_x);
        let result = I1024::from_words(PI_HALF_Q512) - atan_inv;
        return if is_neg { I1024::zero() - result } else { result };
    }

    // Reduction 2: If |x| > tan(π/8), atan(x) = π/4 + atan((x-1)/(x+1))
    let tan_pi8 = I1024::from_words(TAN_PI_8_Q512);
    if abs_x > tan_pi8 {
        let num = abs_x - one_q512;
        let den = abs_x + one_q512;
        let reduced = {
            use crate::fixed_point::domains::binary_fixed::i2048::i2048_div;
            let num_wide = I2048::from_i1024(num) << 512;
            let den_wide = I2048::from_i1024(den);
            i2048_div(num_wide, den_wide).as_i1024()
        };
        let atan_reduced = atan_core_q512_512(reduced);
        let result = I1024::from_words(PI_QUARTER_Q512) + atan_reduced;
        return if is_neg { I1024::zero() - result } else { result };
    }

    // Fallback: |x| <= tan(π/8), call core directly
    let result = atan_core_q512_512(abs_x);
    if is_neg { I1024::zero() - result } else { result }
}

#[cfg(table_format = "q256_256")]
fn atan_core_q512_512(x: I1024) -> I1024 {
    use super::ln_tier_n_plus_1::multiply_i1024_q512_512;

    // x is abs(input) from atan_q512_512, should be positive,
    // but use signed multiply for safety (Horner intermediate results can be negative)
    let x_sq = multiply_i1024_q512_512(x, x);

    // For Q512.512 (512 fractional bits), with |x_sq| ≤ 0.1716:
    // Need n such that 0.1716^n < 2^{-512} → n > 512*ln(2)/ln(1/0.1716) ≈ 201
    let num_terms = 210;
    let mut result = I1024::zero();

    for k in (1..num_terms).rev() {
        // Coefficient 1/(2k+1) computed inline
        let n = (2 * k + 1) as i128;
        let coeff = {
            use crate::fixed_point::domains::binary_fixed::i2048::i2048_div;
            let one_shifted = I2048::from_i1024(I1024::from_i128(1)) << 512;
            let n_wide = I2048::from_i1024(I1024::from_i128(n));
            i2048_div(one_shifted, n_wide).as_i1024()
        };

        // SIGNED multiply: result can be negative in Horner scheme
        let term = multiply_i1024_q512_512(x_sq, result);

        result = coeff - term;
    }

    // Final: atan(x) = x * (1 - x² * result)
    let one_q512 = I1024::from_i128(1) << 512;
    let inner = multiply_i1024_q512_512(x_sq, result);
    let poly = one_q512 - inner;

    // SIGNED multiply: x could theoretically be negative (from reduced argument)
    multiply_i1024_q512_512(x, poly)
}

#[cfg(table_format = "q256_256")]
fn atan2_q512_512(y: I1024, x: I1024) -> I1024 {
    let zero = I1024::zero();
    if x == zero && y == zero {
        return zero;
    }

    if x > zero {
        let ratio = {
            use crate::fixed_point::domains::binary_fixed::i2048::i2048_div;
            let y_wide = I2048::from_i1024(y) << 512;
            let x_wide = I2048::from_i1024(x);
            i2048_div(y_wide, x_wide).as_i1024()
        };
        return atan_q512_512(ratio);
    }

    let pi = I1024::from_words(PI_Q512);
    let y_is_neg = (y.words[15] & 0x8000_0000_0000_0000) != 0;
    let x_is_neg = (x.words[15] & 0x8000_0000_0000_0000) != 0;

    if x_is_neg && !y_is_neg {
        let ratio = {
            use crate::fixed_point::domains::binary_fixed::i2048::i2048_div;
            let y_wide = I2048::from_i1024(y) << 512;
            let x_wide = I2048::from_i1024(x);
            i2048_div(y_wide, x_wide).as_i1024()
        };
        return atan_q512_512(ratio) + pi;
    }

    if x_is_neg && y_is_neg {
        let ratio = {
            use crate::fixed_point::domains::binary_fixed::i2048::i2048_div;
            let y_wide = I2048::from_i1024(y) << 512;
            let x_wide = I2048::from_i1024(x);
            i2048_div(y_wide, x_wide).as_i1024()
        };
        return atan_q512_512(ratio) - pi;
    }

    let pi_half = I1024::from_words(PI_HALF_Q512);
    if !y_is_neg { pi_half } else { zero - pi_half }
}

