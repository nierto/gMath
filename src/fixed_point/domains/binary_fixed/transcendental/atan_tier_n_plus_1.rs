#[cfg(any(table_format = "q64_64", table_format = "q256_256", table_format = "q32_32", table_format = "q16_16"))]
fn atan_q256_256(x: I512) -> I512 {
    let one_q256 = I512::from_i128(1) << 256;

    if x == I512::zero() {
        return I512::zero();
    }

    let is_neg = x < I512::zero();
    let abs_x = if is_neg { I512::zero() - x } else { x };

    if abs_x > one_q256 {
        let inv_x = {
            let one_wide = I1024::from_i512(one_q256) << 256;
            let x_wide = I1024::from_i512(abs_x);
            (one_wide / x_wide).as_i512()
        };
        // Recurse through full reduction (inv_x < 1, so won't re-enter this branch)
        let atan_inv = atan_q256_256(inv_x);
        let result = I512::from_words(PI_HALF_Q256) - atan_inv;
        return if is_neg { I512::zero() - result } else { result };
    }

    let tan_pi8 = I512::from_words(TAN_PI_8_Q256);
    if abs_x > tan_pi8 {
        let num = abs_x - one_q256;
        let den = abs_x + one_q256;
        let reduced = {
            let num_wide = I1024::from_i512(num) << 256;
            let den_wide = I1024::from_i512(den);
            (num_wide / den_wide).as_i512()
        };
        let atan_reduced = atan_core_q256_256(reduced);
        let result = I512::from_words(PI_QUARTER_Q256) + atan_reduced;
        return if is_neg { I512::zero() - result } else { result };
    }

    let result = atan_core_q256_256(abs_x);
    if is_neg { I512::zero() - result } else { result }
}

#[cfg(any(table_format = "q64_64", table_format = "q256_256", table_format = "q32_32", table_format = "q16_16"))]
fn atan_core_q256_256(x: I512) -> I512 {
    let x_sq = {
        let x_wide = I1024::from_i512(x);
        ((x_wide * x_wide) >> 256).as_i512()
    };

    // For Q256.256 storage precision (256 fractional bits), with |x_sq| ≤ tan(π/8)² ≈ 0.1716:
    // Need n such that 0.1716^n < 2^{-256} → n > 256*ln(2)/ln(1/0.1716) ≈ 101
    let num_terms = 105;
    let mut result = I512::zero();

    for k in (1..num_terms).rev() {
        let n = 2 * k + 1;
        // 1/n in Q256.256
        let coeff = {
            let one_wide = I1024::from_i512(I512::from_i128(1)) << 256;
            let n_wide = I1024::from_i512(I512::from_i128(n as i128));
            (one_wide / n_wide).as_i512()
        };
        let term = {
            let x_sq_wide = I1024::from_i512(x_sq);
            let result_wide = I1024::from_i512(result);
            ((x_sq_wide * result_wide) >> 256).as_i512()
        };
        result = coeff - term;
    }

    let one_q256 = I512::from_i128(1) << 256;
    let inner = {
        let x_sq_wide = I1024::from_i512(x_sq);
        let result_wide = I1024::from_i512(result);
        ((x_sq_wide * result_wide) >> 256).as_i512()
    };
    let poly = one_q256 - inner;

    let x_wide = I1024::from_i512(x);
    let poly_wide = I1024::from_i512(poly);
    ((x_wide * poly_wide) >> 256).as_i512()
}

